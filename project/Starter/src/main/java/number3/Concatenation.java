package number3;

public class Concatenation {
    public static void main(String[] args) {
        String word = "Soli";
        String word2 = " Deo";
        String word3 = " Gloria!";
        String phrases = word+word2+word3;
        System.out.println(phrases);

        System.out.println("Soli Deo Gloria!");
    }
}
