package Starter.number2;

public class Char {
    public static void main(String[] args) {
        char a = 'I';// Символ
        char a1 = 0x0041;// Значение в 16-ричном формате
        char a2 = '\u0065';// Значение в формате unicode

        System.out.println(a);
        System.out.println(a1);
        System.out.println(a2);

        int f =7/7;
        System.out.println(f);
    }
}
